function [T_order, T] = dynamic_resid_tt(y, x, params, steady_state, T_order, T)
if T_order >= 0
    return
end
T_order = 0;
if size(T, 1) < 39
    T = [T; NaN(39 - size(T, 1), 1)];
end
T(1) = exp(y(3))*exp(y(18))/exp(y(17));
T(2) = exp(y(22))^(1-y(19));
T(3) = ((1-y(19))*(1-y(19)))^(1-y(19));
T(4) = (T(3))^(1-params(1));
T(5) = T(1)^((1-params(1))*(y(19)-y(20)));
T(6) = (params(8)/params(7))^params(1);
T(7) = exp(y(23))^(1-y(20));
T(8) = y(19)/y(20);
T(9) = T(8)^y(20);
T(10) = ((1-y(19))*(1-y(20)))^(1-y(20));
T(11) = (T(2)/T(7)*T(9)*T(10))^(1-params(1));
T(12) = T(1)^((1-params(1))*(y(19)-y(21)));
T(13) = (params(9)/params(7))^params(1);
T(14) = exp(y(24))^(1-y(21));
T(15) = y(19)/y(21);
T(16) = T(15)^y(21);
T(17) = ((1-y(19))*(1-y(21)))^(1-y(21));
T(18) = (T(2)/T(14)*T(16)*T(17))^(1-params(1));
T(19) = T(4)+T(5)*T(6)*T(11)+T(12)*T(13)*T(18);
T(20) = exp(y(3)*exp(y(18))/exp(y(17)));
T(21) = T(20)^((1-params(1))*(y(19)-y(20)));
T(22) = T(6)*y(20)*T(21);
T(23) = T(20)^((1-params(1))*(y(19)-y(21)));
T(24) = T(13)*y(21)*T(23);
T(25) = exp(y(15))*exp(y(30))/exp(y(29));
T(26) = T(25)^(y(31)-1);
T(27) = exp(y(13))*params(2)*(1+y(31)*T(26)-params(3));
T(28) = exp(y(13))/exp(y(16));
T(29) = (exp(y(3))*exp(y(18)))^y(19);
T(30) = (exp(y(17))*exp(y(22)))^(1-y(19));
T(31) = exp(y(3))^y(19);
T(32) = (1-y(19))*T(2)*T(31);
T(33) = exp(y(18))/exp(y(17));
T(34) = T(33)^y(19);
T(35) = T(32)*T(34);
T(36) = T(28)*(1-1/T(19));
T(37) = 1-T(36);
T(38) = y(19)*T(4)+T(11)*T(22)+T(18)*T(24)-y(19)*T(19);
T(39) = T(28)*T(38)/T(19);
end
