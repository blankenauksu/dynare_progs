function [lhs, rhs] = static_resid(y, x, params)
T = NaN(13, 1);
lhs = NaN(10, 1);
rhs = NaN(10, 1);
T(1) = (params(1)-1)/params(1);
T(2) = params(5)*y(5)^T(1)+params(6)*y(6)^T(1)+params(7)*y(7)^T(1);
T(3) = T(2)^((1-params(1)*params(2))/(params(1)-1));
T(4) = (-1)/params(1);
T(5) = params(5)*y(5)^T(4);
T(6) = T(3)*T(5);
T(7) = params(6)*y(6)^T(4);
T(8) = T(3)*T(7);
T(9) = params(7)*y(7)^T(4);
T(10) = T(3)*T(9);
T(11) = y(4)^(params(3)-1);
T(12) = T(6)*exp(y(8))*y(1)*params(3)*T(11)/y(1);
T(13) = y(4)^params(3);
lhs(1) = T(8)*T(11)*params(3)*exp(y(9))*y(2);
rhs(1) = y(2)*T(12);
lhs(2) = T(10)*T(11)*params(3)*exp(y(10))*y(3);
rhs(2) = y(3)*T(12);
lhs(3) = params(4)*(1+exp(y(8))*y(1)*params(3)*T(11)-params(8));
rhs(3) = 1;
lhs(4) = 1;
rhs(4) = y(3)+y(1)+y(2);
lhs(5) = y(5);
rhs(5) = exp(y(8))*y(1)*T(13)+y(4)*(1-params(8))-y(4);
lhs(6) = y(6);
rhs(6) = exp(y(9))*y(2)*T(13);
lhs(7) = y(7);
rhs(7) = exp(y(10))*y(3)*T(13);
lhs(8) = y(8);
rhs(8) = y(8)*params(12)+x(1);
lhs(9) = y(9);
rhs(9) = y(9)*params(13)+x(2);
lhs(10) = y(10);
rhs(10) = y(10)*params(14)+x(3);
end
