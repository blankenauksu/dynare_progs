function [T_order, T] = dynamic_resid_tt(y, x, params, steady_state, T_order, T)
if T_order >= 0
    return
end
T_order = 0;
if size(T, 1) < 33
    T = [T; NaN(33 - size(T, 1), 1)];
end
T(1) = y(3)*y(18)/y(17);
T(2) = exp(y(22))^(1-y(19));
T(3) = ((1-y(19))*(1-y(19)))^(1-y(19));
T(4) = (T(3))^(1-params(1));
T(5) = T(1)^((1-params(1))*(y(19)-y(20)));
T(6) = (params(8)/params(7))^params(1);
T(7) = exp(y(23))^(1-y(20));
T(8) = y(19)/y(20);
T(9) = T(8)^y(20);
T(10) = ((1-y(19))*(1-y(20)))^(1-y(20));
T(11) = (T(2)/T(7)*T(9)*T(10))^(1-params(1));
T(12) = T(1)^((1-params(1))*(y(19)-y(21)));
T(13) = (params(9)/params(7))^params(1);
T(14) = exp(y(24))^(1-y(21));
T(15) = y(19)/y(21);
T(16) = T(15)^y(21);
T(17) = ((1-y(19))*(1-y(21)))^(1-y(21));
T(18) = (T(2)/T(14)*T(16)*T(17))^(1-params(1));
T(19) = T(4)+T(5)*T(6)*T(11)+T(12)*T(13)*T(18);
T(20) = (y(15)*y(30)/y(29))^(y(31)-1);
T(21) = 1+y(31)*T(20)-params(3);
T(22) = y(13)/y(16);
T(23) = (y(18)*y(15))^y(19);
T(24) = (y(17)*exp(y(22)))^(1-y(19));
T(25) = y(15)^y(19);
T(26) = (1-y(19))*T(2)*T(25);
T(27) = y(18)/y(17);
T(28) = T(27)^y(19);
T(29) = T(26)*T(28);
T(30) = 1-T(22)*(1-1/T(19));
T(31) = y(19)*T(4)+T(11)*T(6)*y(20)*T(5)+T(18)*T(13)*y(21)*T(12)-y(19)*T(19);
T(32) = T(31)/T(19);
T(33) = T(22)*T(32);
end
